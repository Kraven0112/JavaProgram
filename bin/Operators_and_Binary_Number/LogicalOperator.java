package Operators_and_Binary_Number;

public class LogicalOperator {
    public static void main(String[]args){
        int a = 10;
        int b = 20;
        int c = 30;

        if(a<b && a<c){
            System.out.println(true);
        }

        if(b>a || b>40){
            System.out.println(true);
        }

    }
}
