package Operators_and_Binary_Number;

public class RelationalOperator {
    public static void main(String[]args){
        int a = 10;
        int b = 20;
        int c = 30;

        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>c);
        System.out.println(a>=c);
        System.out.println(a<=c);
    }
}
