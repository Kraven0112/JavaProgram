package Operators_and_Binary_Number;

public class BitwiseOperator {
    public static void main(String[]args){
        int a = 2;
        int b = 3;

        //bitwise AND(&)
        System.out.println(a&b);

        //bitwise OR(|)
        System.out.println(a|b);

        //bitwise XOR(^)
        System.out.println(12^8);

        //bitwise complement(~)
        System.out.println(~a);

        //bitwise shiftleft(<<)
        System.out.println(a<<1);

        //bitwise shiftright
        System.out.println(a>>1);

    }
}
